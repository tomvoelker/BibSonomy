/**
 * Enum for the search types
 *
 * @author lutful
 */
public enum SearchType {

	/**
	 * search public posts of all connected BibSonomy-based systems (including PUMA)
	 */
	CROSS_SYSTEM_SEARCH, 
	/**
	 * search all viewable posts on the local system
	 */
	DEFAULT_SEARCH;
}
