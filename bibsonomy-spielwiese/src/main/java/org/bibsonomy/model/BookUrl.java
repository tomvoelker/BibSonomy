public class BookUrl{ 
	
	private  String book_url_hash; 
	private  String book_url;
	private int book_url_ctr;
	
	
	
	public String getBook_url_hash() {
		return this.book_url_hash;
	}

	public void setBook_url_hash(String book_url_hash) {
		this.book_url_hash = book_url_hash;
	}

	public String getBook_url() {
		return this.book_url;
	}

	public void setBook_url(String book_url) {
		this.book_url = book_url;
	}

	public int getBook_url_ctr() {
		return this.book_url_ctr;
	}

	public void setBook_url_ctr(int book_url_ctr) {
		this.book_url_ctr =book_url_ctr;
	}

	
	
	
	
	
	
	
}